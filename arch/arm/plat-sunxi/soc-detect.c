#include <linux/module.h>
#include <linux/io.h>

#include <asm/mach-types.h>

#include <plat/platform.h>
#include <plat/system.h>

struct brom_header {
	u32 jump_instruction;	/* one intruction jumping to real code */
	char magic[8];		/* ="eGON.BRM",  not C-style string. */
	u32 length;		/* generated by PC */
	char Boot_vsn[4];	/* Boot version */
	char eGON_vsn[4];	/* eGON version */
	char platform[8];	/* platform information */
};

static inline const struct brom_header *_sunxi_brom(void)
{
	const struct brom_header *brom = (void*)SW_VA_BROM_BASE + 0x4000;
	if (strncmp(brom->magic, "eGON.BRM", 8) == 0)
		return brom;
	else
		return NULL;
}

int sunxi_pr_brom(void)
{
	const struct brom_header *brom = _sunxi_brom();
	if (brom) {
		pr_info("BROM Ver: %.4s %.4s %.8s\n",
			brom->Boot_vsn,
			brom->eGON_vsn,
			brom->platform);
		return 1;
	}
	pr_err("SUNXI BROM not found");
	return 0;
}

u32 sunxi_chip_id(void)
{
	const struct brom_header *brom = _sunxi_brom();
	if (brom) {
		char *endptr;
		u32 chip_id = simple_strtoul(brom->platform, &endptr, 10);
		if (*endptr == '\0')
			return chip_id;

		pr_err("BROM: failed to parse chip-id: \"%s\"\n",
		       brom->platform);
	}
	return 0;
}

int sunxi_pr_chip_id()
{
	u32 chip_id = sunxi_chip_id();
	const char *name;

	switch (chip_id) {
	case SUNXI_CHIP_ID_A10:
		name = "A10"; break;
	case SUNXI_CHIP_ID_A13:
		name = "A13"; break;
	default:
		name = "Unknown";
	}

	if (machine_is_sun4i()) {
		int rev = sw_get_ic_ver() - SUNXI_VER_A10A;
		pr_info("chip-id: %s (AW%u revision %c)\n", name,
			chip_id, 'A' + rev);
	} else
		pr_info("chip-id: %s (AW%u)\n", name, chip_id);

	return (chip_id != 0);
}

enum sw_ic_ver sw_get_ic_ver(void)
{
	static enum sw_ic_ver ver;

	if (likely(ver))
		goto done;

	if (machine_is_sun4i()) {
		u32 val = readl(SW_VA_TIMERC_IO_BASE + 0x13c);
		val = (val >> 6) & 0x3;

		if (val == 0)
			ver = SUNXI_VER_A10A;
		else if (val == 3)
			ver = SUNXI_VER_A10B;
		else
			ver = SUNXI_VER_A10C;
		goto done;

	} else if (machine_is_sun5i()) {
		u32 val = readl(SW_VA_SSE_IO_BASE);
		val = (val >> 16) & 0x07;

		if (val == 0)
			ver = SUNXI_VER_A13A;
		else if (val == 1) {
			val = readl(SW_VA_SID_IO_BASE+0x08);
			val = (val >> 12) & 0xf;

			if (val == 0 || val == 3)
				ver = SUNXI_VER_A12A;
			else if (val == 7)
				ver = SUNXI_VER_A10SA;
		}

		if (!ver)
			goto unknown_chip;

		val = readl(SW_VA_SID_IO_BASE+0x00);
		val = (val >> 8) & 0xffffff;

		if (val == 0 || val == 0x162541)
			; /* rev A */
		else if (val == 0x162542)
			ver++; /* rev B */
		else {
			const char *name;
			if (ver == SUNXI_VER_A13A)
				name = "A13";
			else if (ver == SUNXI_VER_A12A)
				name = "A12";
			else
				name = "A10S";

			pr_err("sunxi: unrecongnized %s revision (%x)\n",
			       name, val);

			goto unknown;
		}
	}

unknown_chip:
	pr_err("sunxi: unrecognized IC\n");
unknown:
	ver = SUNXI_VER_UNKNOWN;
done:
	return ver;
}
EXPORT_SYMBOL(sw_get_ic_ver);
